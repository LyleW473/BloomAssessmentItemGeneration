import json
import numpy as np
import math
import re
from collections import Counter
from openai import OpenAI
from typing import Dict, Any, List, Union
from src.llm_response_generation.functions import generate_llm_response, extract_json_from_text
from src.evaluation.assessment.instructions import (
    CRITERION_INSTRUCTIONS_MAPPING,
    TRUE_BLOOM_LEVEL_INSTRUCTION,
    MARK_RANGES
)
from src.data_processing.synthetic.bloom_system_instructions.shared.generation import BLOOM_LEVEL_TO_DIFFICULTY_MAPPING

class CriterionVerifier:
    """
    Class to perform verification checks on defined criterions for generated SAQs.
    This is used as part of the pairwise evaluation process for comparing questions generated by different pipelines.

    This class assesses two types of criterions:
    1. STEM verification: Evaluates question stem quality (Bloom alignment, course grounding, clarity, etc.)
    2. TRIPLE verification: Evaluates the complete triple (question + mark scheme + expected answer)
    """

    def __init__(self, client:OpenAI, model_names:List[str]):
        """
        Initializes the CriterionVerifier with the given OpenAI client and model names.

        Args:
            client (OpenAI): The OpenAI client instance for making API calls.
            model_names (List[str]): The names of the models to use for verification tasks.
        """
        self.client = client
        self.model_names = model_names
        self.bloom_to_indices = {
            "knowledge": 0,
            "understanding": 1,
            "application": 2,
            "analyze": 3,
            "evaluation": 4,
            "synthesis": 5
        }

    def _perform_verification(
        self,
        question:str,
        true_bloom_level:str,
        declared_bloom_level:str,
        extracted_text:str,
        scenario:Union[str, None],
        mark_scheme:List[Dict[str, Any]],
        expected_answer:str,
        max_retries:int=3
    ) -> Dict[str, Any]:
        """
        Performs evaluation for both STEM and TRIPLE criterions using the respective system instructions and input formats,
        using multiple different models. Each criterion is evaluated independently with its specific instruction, and the 
        results are aggregated across models.

        This performs evaluation of:
        - Stem:
            - Bloom alignment check
            - Course grounding check
            - Clarity check
            - Single task integrity check
        - Triple:
            - Objective gradability (triple-aware, considering mark scheme)
            - Mark scheme quality
            - Answer fidelity (coverage of mark scheme)
            - Grounding of mark scheme in course content
        - Scenario (if scenario is provided):
            - Scenario relevance and necessity
            - Scenario grounding in course content
        
        Args:
            question (str): The question text
            true_bloom_level (str): The true Bloom level inferred for the question based on its content and the course material
            declared_bloom_level (str): The declared Bloom level
            extracted_text (str): The course content text
            mark_scheme (list): List of mark scheme points (dicts with 'point' and 'marks')
            expected_answer (str): The expected answer text
            max_retries (int): Maximum number of retry attempts on failure

        Returns:
            Dict[str, Any]: Verification results containing:
                - scores: Dict with scores (0-2) for each criterion
                - mark_scheme_issues: List of specific issues found in mark scheme
                - answer_issues: List of specific issues found in answer
                - reasons: Dict with 1-sentence reasons for each score
        """

        stem_input = (
            {
                "question": question,
                "true_bloom_level": true_bloom_level,
                "declared_bloom_level": declared_bloom_level,
                "extracted_text": extracted_text
            }
        )

        triple_input = { # All triple criterions have same input
            "question": question,
            "true_bloom_level": true_bloom_level,
            "declared_bloom_level": declared_bloom_level,
            "extracted_text": extracted_text,
            "mark_scheme": mark_scheme,
            "expected_answer": expected_answer
        }
        
        scenario_input = {
            "scenario": scenario,
            "question": question,
            "true_bloom_level": true_bloom_level,
            "declared_bloom_level": declared_bloom_level,
            "extracted_text": extracted_text,
        }

        input_types = {
            "stem": stem_input,
            "triple": triple_input,
        }
        if scenario is not None: # Only include scenario-based criterions if a scenario is provided in the input
            input_types["scenario"] = scenario_input

        outputs = {}
        for criterion_type, input_data in input_types.items():
            instruction_mappings = CRITERION_INSTRUCTIONS_MAPPING[criterion_type] # Get instruction mappings for this criterion type (stem or triple)
            prompt_text = json.dumps(input_data, indent=4) # Create prompt text for this criterion type (same for all criterions of this type)

            for criterion, instruction in instruction_mappings.items():

                for model_name in self.model_names:

                    if criterion not in outputs:
                        outputs[criterion] = {}

                    if criterion == "mark_scheme_quality":
                        # Check if the mark scheme is consistent with the true range of marks for the true Bloom level of the question. If not, assign a score of 0 without calling the model, as this is an objective criterion that can be checked with a simple rule based on the true Bloom level.
                        true_bloom_level_mark_range = MARK_RANGES[true_bloom_level]
                        # print(f"Checking 'mark_scheme_quality' criterion for model '{model_name}' with true Bloom level '{true_bloom_level}' (mark range: {true_bloom_level_mark_range} marks)")
                        
                        total_marks = sum(point.get("marks", 0) for point in mark_scheme)
                        # print(f"Total marks in mark scheme: {total_marks}")

                        if not (true_bloom_level_mark_range[0] <= total_marks <= true_bloom_level_mark_range[1]):
                            outputs[criterion][model_name] = 0
                            continue

                    last_error = None
                    for attempt in range(max_retries):
                        # print(f"Verifiying model '{model_name}' for criterion '{criterion}' (attempt {attempt + 1}/{max_retries})...")
                        try:
                            messages = [
                                {"role": "system", "content": instruction},
                                {"role": "user", "content": prompt_text}
                            ]
                            criterion_result = generate_llm_response(
                                client=self.client,
                                model_name=model_name,
                                messages=messages
                            )
                            assert criterion_result is not None and (isinstance(criterion_result, str) or isinstance(criterion_result, int)), f"Criterion result must be a string or integer, got {type(criterion_result)}"
                            if isinstance(criterion_result, str):
                                criterion_result = criterion_result.strip()
                                # Fast path: already a bare digit
                                if criterion_result not in ("0", "1", "2"):
                                    # Try to extract the first standalone 0/1/2 digit from the response.
                                    # This handles models that prefix or suffix their score with explanatory text
                                    # (e.g. "2\n\nThe question asks..." or "Score: 1" or "**1**").
                                    match = re.search(r'\b([012])\b', criterion_result)
                                    if match:
                                        criterion_result = match.group(1)
                                    else:
                                        # Last resort: keep only digits and hope there is exactly one valid value
                                        digits_only = ''.join(filter(str.isdigit, criterion_result))
                                        if digits_only in ("0", "1", "2"):
                                            criterion_result = digits_only

                            if criterion_result not in ["0", "1", "2", 0, 1, 2]:
                                raise ValueError(f'Score for "{criterion}" must be 0, 1, or 2 (as string or integer), got: {criterion_result}')
                            outputs[criterion][model_name] = int(criterion_result)
                            # print(f"Verification for criterion '{criterion}' successful with score: {criterion_result}")
                            break # Break out of retry loop on success

                        except (ValueError, KeyError, TypeError, AssertionError) as e:
                            last_error = e
                            print(f"Verification attempt {attempt + 1}/{max_retries} failed: {e}")
                            if attempt < max_retries - 1:
                                print(f"Retrying...")
                                continue
                            else:
                                print(f"All retry attempts exhausted for verification")
                                raise RuntimeError(f"Verification failed after {max_retries} attempts: {last_error}") from last_error
        
        # print(f"Verification results:\n{json.dumps(outputs, indent=4)}")
        return outputs

    def _get_true_bloom_level(self, question:str, extracted_text:str, max_retries:int=3) -> Dict[str, str]:
        """
        Uses a dedicated prompt to infer the true Bloom level of the question based on its content and the course material.

        Args:
            question (str): The question text.
            extracted_text (str): The course content text.
            max_retries (int): Maximum number of retry attempts per model on failure.
        Returns:
            Dict[str, str]: A dictionary mapping each model name to the inferred true Bloom level for the question.
        """
        _valid_levels = set(self.bloom_to_indices.keys())

        def _extract_bloom_level(raw: str) -> str:
            """
            Try multiple strategies to extract a valid Bloom level keyword from a
            potentially verbose model response (e.g. reasoning text ending with
            'The final answer is $\\boxed{evaluation}$').
            Returns the canonical level string, or raises ValueError if none found.
            """
            if not isinstance(raw, str):
                raise ValueError(f"Expected string, got {type(raw)}: {raw!r}")

            raw_stripped = raw.strip()

            # Strategy 0: Remove 'Label:' prefix if present
            if raw_stripped.lower().startswith("label:"):
                raw_stripped = raw_stripped[len("label:"):].strip()

            # Strategy 1: Exact match after basic normalisation
            candidate = raw_stripped.lower()
            if candidate == "analysis":
                candidate = "analyze"
            if candidate in _valid_levels:
                return candidate

            # Strategy 2: JSON extraction (handles {"bloom_level": "application"} etc.)
            try:
                extracted = extract_json_from_text(raw_stripped)
                if isinstance(extracted, str):
                    val = extracted.strip().lower()
                    if val == "analysis":
                        val = "analyze"
                    if val in _valid_levels:
                        return val
                elif isinstance(extracted, dict):
                    for key in ("label", "result", "bloom_level"):
                        if key in extracted:
                            val = str(extracted[key]).strip().lower()
                            if val == "analysis":
                                val = "analyze"
                            if val in _valid_levels:
                                return val
                    if len(extracted) == 1:
                        val = str(next(iter(extracted.values()))).strip().lower()
                        if val == "analysis":
                            val = "analyze"
                        if val in _valid_levels:
                            return val
            except Exception:
                pass

            # Strategy 3: Find the LAST occurrence of any valid level word in the text.
            # This handles verbose reasoning that concludes with the answer (e.g. "... is **evaluation**"), "analysis" is treated as an alias for "analyze".
            pattern = r'\b(knowledge|understanding|application|analyz[ei]s?|analysis|evaluation|synthesis)\b'
            matches = re.findall(pattern, raw_stripped, flags=re.IGNORECASE)
            if matches:
                val = matches[-1].lower()
                if val in ("analysis", "analyse", "analyzes"):
                    val = "analyze"
                if val in _valid_levels:
                    return val

            raise ValueError(f"'bloom_level' should be one of {list(_valid_levels)}, could not extract from: {raw_stripped!r}")

        out = {}
        for model in self.model_names:
            prompt_text = json.dumps({
                "question": question,
                "extracted_text": extracted_text
            }, indent=4)
            messages = [
                {"role": "system", "content": TRUE_BLOOM_LEVEL_INSTRUCTION},
                {"role": "user", "content": prompt_text}
            ]

            last_error = None
            for attempt in range(max_retries):
                try:
                    raw = generate_llm_response(
                        client=self.client,
                        model_name=model,
                        messages=messages
                    )
                    if raw is None:
                        raise ValueError("'bloom_level' response was None")
                    bloom_level = _extract_bloom_level(raw)
                    out[model] = bloom_level
                    break # Success
                except (ValueError, KeyError, TypeError, AssertionError) as e:
                    last_error = e
                    print(f"Bloom level extraction attempt {attempt + 1}/{max_retries} failed for model '{model}': {e}")
                    if attempt < max_retries - 1:
                        print("Retrying...")
                        continue
                    else:
                        print(f"All retry attempts exhausted for model '{model}' bloom level extraction")
                        raise RuntimeError(
                            f"Bloom level extraction failed after {max_retries} attempts for model '{model}': {last_error}"
                        ) from last_error

        return out

    def _get_bloom_alignment_dict(
            self,
            true_bloom_level_per_model:str,
            declared_bloom_level:str,
        ) -> Dict[str, Any]:

        bloom_alignments_per_model = {}

        for model in self.model_names:
            true_bloom_level = true_bloom_level_per_model[model]

            # Gap between declared and true bloom level
            min_gap = abs(self.bloom_to_indices[true_bloom_level] - self.bloom_to_indices[declared_bloom_level])

            # Bloom alignment score for this model
            alignment_score = 1 - (min_gap / (len(self.bloom_to_indices) - 1)) # Normalize gap to [0,1] and invert so that 1 means perfect alignment and 0 means worst alignment

            bloom_alignments_per_model[model] = {
                "declared_bloom_level": declared_bloom_level,
                "true_bloom_level": true_bloom_level,
                "gap": min_gap,
                "bloom_alignment_score": alignment_score
            }
        # print(f"\nBloom alignments per model:\n{json.dumps(bloom_alignments_per_model, indent=4)}")
        # print()

        # Select true bloom level as the majority vote (Deterministic majority with ordinal tie-break)
        levels = list(true_bloom_level_per_model.values())
        counts = Counter(levels)
        top = max(counts.values())
        tied = [lvl for lvl, c in counts.items() if c == top]
        if len(tied) == 1:
            true_bloom_level_majority = tied[0] # Clear majority, no tie
        else:
            avg_idx = sum(self.bloom_to_indices[l] for l in tied) / len(tied)
            true_bloom_level_majority = {v:k for k,v in self.bloom_to_indices.items()}[math.floor(avg_idx)] # Tie-break by taking the level with the lowest index (most basic level) among the tied levels
        
        # print(f"\nTrue Bloom levels per model: {true_bloom_level_per_model}")
        # print(f"Majority vote for true Bloom level: {true_bloom_level_majority}")

        # Select the median bloom alignment score
        bloom_alignment_scores = [info["bloom_alignment_score"] for info in bloom_alignments_per_model.values()]
        alignment_score = np.median(bloom_alignment_scores)

        # Calculate Shannon-entropy of Bloom alignment scores
        counts = Counter(levels)
        total = len(levels)
        entropy = 0.0
        for count in counts.values():
            p = count / total
            entropy -= p * math.log(p, 2)
        # print(f"Shannon entropy of Bloom level distribution: {entropy:.4f} bits (0 means all models agree, higher means more disagreement)")
        
        # Calculate normalized entropy
        entropy_normalized = entropy / math.log2(len(self.bloom_to_indices)) # Normalize by max possible entropy (if all 6 levels were equally represented)
        # print(f"Normalized entropy of Bloom level distribution: {entropy_normalized:.4f}")

        # Alignment metrics
        alignment_spread = float(max(bloom_alignment_scores) - min(bloom_alignment_scores)) # Spread between best and worst alignment scores across models
        alignment_mean = float(np.mean(bloom_alignment_scores))

        result = {
            "bloom_alignments_per_model": bloom_alignments_per_model,
            "declared_bloom_level": declared_bloom_level,
            "majority_true_bloom_level": true_bloom_level_majority, # Selected through voting
            "bloom_alignment_score": alignment_score, # Selected through median to be robust to outliers across models
            "bloom_level_entropy": entropy,
            "bloom_level_entropy_normalized": entropy_normalized,
            "alignment_spread": alignment_spread,
            "alignment_mean": alignment_mean,
            "vote_counts": dict(counts)
        }
        # print(f"\nResult:\n{json.dumps(result, indent=4)}")
        # print()
        return result

    def perform_checks(
        self,
        generated_question_dict: Dict[str, Any],
        extracted_text: str,
        max_retries: int = 5
    ) -> Dict[str, Any]:
        """
        Performs comprehensive verifcation checks using defined criterions to assess the quality of generated
        question stems (stems) and complete assessment triples (question + mark scheme + expected answer).

        This method:
        1. Runs verification on all 'stem' type criterions to get scores.
        2. Runs verification on all 'triple' type criterions to get scores.
        3. Normalizes scores to a 0-1 scale.

        Args:
            generated_question_dict (Dict[str, Any]): Dictionary containing:
                - question: The question text
                - bloom_level: Declared Bloom level (optional, defaults to "unknown")
                - mark_scheme: List of mark scheme points
                - expected_answer: Expected answer text
                - difficulty: Difficulty level (used for logging only)
            extracted_text (str): The course content text
            max_retries (int): Maximum number of retry attempts on failure

        Returns:
            Dict[str, Any]: Combined verification results with:
                - raw_scores: Dict with raw scores (0-2) for each criterion from both STEM and TRIPLE verification
                - metrics: Normalized metrics (0-1 scale) for each criterion.
        
        Raises:
            RuntimeError: If verification fails after all retry attempts
        """
        question = generated_question_dict["question"]
        difficulty = generated_question_dict["difficulty"].lower()
        declared_bloom_level = generated_question_dict.get("bloom_level", None)
        mark_scheme = generated_question_dict["mark_scheme"]
        expected_answer = generated_question_dict["expected_answer"]

        # Check for scenario (SBQ-specific)
        scenario = generated_question_dict.get("scenario", None)

        # Get the true Bloom level for this question
        true_bloom_level_per_model = self._get_true_bloom_level(
            question=question,
            extracted_text=extracted_text
        )
        # print(f"\nTrue Bloom level inferred: {true_bloom_level_per_model}")
        
        # Correct declared Bloom level if needed
        if declared_bloom_level not in self.bloom_to_indices:
            declared_bloom_level_source = "from_difficulty_key"
            # Take lower bound bloom level
            # Avoids over-crediting pipelines that didn't declare a Bloom level.
            # print(f"Declared Bloom level '{declared_bloom_level}' is invalid or missing. It should be one of {list(self.bloom_to_indices.keys())}. Will use 'difficulty' to determine declared Bloom level for alignment scoring.")
            if difficulty == "easy":
                declared_bloom_level = "knowledge"
            elif difficulty == "medium":
                declared_bloom_level = "application"
            elif difficulty == "hard":
                declared_bloom_level = "evaluation"
        else:
            declared_bloom_level_source = "declared_from_pipeline"

        # Calculate the Bloom alignment score and details based on the true Bloom level, declared Bloom level, and difficulty
        bloom_alignment_dict = self._get_bloom_alignment_dict(
            true_bloom_level_per_model=true_bloom_level_per_model,
            declared_bloom_level=declared_bloom_level,
        )
        bloom_alignment_dict["declared_bloom_level_source"] = declared_bloom_level_source
        # print(f"\nBloom alignment details:\n{json.dumps(bloom_alignment_dict, indent=4)}")

        # Replace the old 'declared_bloom_level' with the new one used for bloom alignment scoring 
        # (Which is either the original declared bloom level if valid, or the closest one based on the 'difficulty' key.
        declared_bloom_level = bloom_alignment_dict["declared_bloom_level"]

        # Find raw scores for all criterions using the respective instructions and inputs, with multiple models for robustness
        # print(f"True bloom level: {bloom_alignment_dict['majority_true_bloom_level']} | Declared bloom level: {declared_bloom_level} (source: {declared_bloom_level_source}) | Bloom alignment score: {bloom_alignment_dict['bloom_alignment_score']:.4f}")
        criterion_outputs = self._perform_verification(
            question=question,
            declared_bloom_level=declared_bloom_level,
            true_bloom_level=bloom_alignment_dict["majority_true_bloom_level"],
            extracted_text=extracted_text,
            scenario=scenario,
            mark_scheme=mark_scheme,
            expected_answer=expected_answer,
            max_retries=max_retries
        )

        # Aggregate raw scores across all models and criterions and compute average scores for each criterion
        metrics = {criterion: sum(model_scores.values()) / len(model_scores) / 2 for criterion, model_scores in criterion_outputs.items()}
        metrics["bloom_level_alignment"] = bloom_alignment_dict["bloom_alignment_score"] # Add bloom alignment score to metrics (0-1 scale)

        # Per-evaluator-model normalized metrics: preserved separately for inter-rater analysis
        # Structure: {evaluator_model_name: {criterion_name: normalized_score (0-1)}}
        metrics_per_model: Dict[str, Dict[str, float]] = {}
        for criterion, model_scores in criterion_outputs.items():
            for model_name, raw_score in model_scores.items():
                metrics_per_model.setdefault(model_name, {})[criterion] = raw_score / 2
        for model_name, bloom_info in bloom_alignment_dict["bloom_alignments_per_model"].items():
            metrics_per_model.setdefault(model_name, {})["bloom_level_alignment"] = bloom_info["bloom_alignment_score"]

        final_result = {
            "raw_scores": criterion_outputs,
            "metrics": metrics,
            "metrics_per_model": metrics_per_model,
            "bloom_alignment_details": bloom_alignment_dict,
        }
        # print(f"\nFinal verification result:\n{json.dumps(final_result, indent=4)}")
        return final_result

    def update_result_with_new_models(
        self,
        existing_result:Dict[str, Any],
        generated_question_dict:Dict[str, Any],
        extracted_text:str,
        new_model_names:List[str],
        max_retries:int=5
    ) -> Dict[str, Any]:
        """
        Adds scores from additional evaluator models to an already-processed criterion result,
        then recomputes all aggregated statistics (majority vote, median alignment, entropy,
        metrics, metrics_per_model) using the full set of old + new models together.

        - This enables incremental addition of new LLM-as-a-judge models without re-running
        the models that have already evaluated a question.
        - The new models' criterion prompts receive the updated majority_true_bloom_level
        (recomputed after merging all models' Bloom predictions), which is the most accurate
        consensus signal available.

        Args:
            existing_result (Dict[str, Any]): The stored criterion result dict for one question (as returned
                             by perform_checks and saved in the JSON cache).
            generated_question_dict (Dict[str, Any]): The original question dict (same one that was passed to
                                     perform_checks when the existing_result was created).
            extracted_text (str): The course content text (same as original).
            new_model_names (List[str]): List of evaluator model names to add. Must not overlap with
                             models already present in existing_result["raw_scores"].
            max_retries (int): Maximum retry attempts per model call.

        Returns:
            Dict[str, Any]: An updated copy of existing_result with new models' scores merged in and all
            aggregated statistics recomputed.
        """
        import copy
        result = copy.deepcopy(existing_result)

        question = generated_question_dict["question"]
        scenario = generated_question_dict.get("scenario", None)
        mark_scheme = generated_question_dict["mark_scheme"]
        expected_answer = generated_question_dict["expected_answer"]
        declared_bloom_level = result["bloom_alignment_details"]["declared_bloom_level"]
        declared_bloom_level_source = result["bloom_alignment_details"].get("declared_bloom_level_source", "unknown")

        # Temporarily restrict to only the new models so existing helpers work unchanged
        original_model_names = self.model_names
        self.model_names = new_model_names
        try:
            # 1. Get Bloom level predictions from the new models
            new_bloom_per_model = self._get_true_bloom_level(
                question=question,
                extracted_text=extracted_text
            )

            # 2. Merge with stored per-model Bloom predictions and recompute aggregated stats
            merged_bloom_per_model = {
                m: info["true_bloom_level"]
                for m, info in result["bloom_alignment_details"]["bloom_alignments_per_model"].items()
            }
            merged_bloom_per_model.update(new_bloom_per_model)

            self.model_names = list(merged_bloom_per_model.keys())  # all models for _get_bloom_alignment_dict
            new_bloom_dict = self._get_bloom_alignment_dict(
                true_bloom_level_per_model=merged_bloom_per_model,
                declared_bloom_level=declared_bloom_level,
            )
            new_bloom_dict["declared_bloom_level_source"] = declared_bloom_level_source

            # 3. Run criterion verification for new models using the updated majority
            self.model_names = new_model_names
            new_criterion_outputs = self._perform_verification(
                question=question,
                declared_bloom_level=declared_bloom_level,
                true_bloom_level=new_bloom_dict["majority_true_bloom_level"],
                extracted_text=extracted_text,
                scenario=scenario,
                mark_scheme=mark_scheme,
                expected_answer=expected_answer,
                max_retries=max_retries
            )
        finally:
            self.model_names = original_model_names

        # 4. Merge new criterion scores into existing raw_scores
        for criterion, model_scores in new_criterion_outputs.items():
            if criterion not in result["raw_scores"]:
                result["raw_scores"][criterion] = {}
            result["raw_scores"][criterion].update(model_scores)

        # 5. Replace bloom_alignment_details with the recomputed version
        result["bloom_alignment_details"] = new_bloom_dict

        # 6. Recompute aggregated metrics (averaged across all models)
        criterion_outputs = result["raw_scores"]
        metrics = {
            criterion: sum(model_scores.values()) / len(model_scores) / 2
            for criterion, model_scores in criterion_outputs.items()
        }
        metrics["bloom_level_alignment"] = new_bloom_dict["bloom_alignment_score"]
        result["metrics"] = metrics

        # 7. Recompute metrics_per_model (per evaluator model, normalised)
        metrics_per_model: Dict[str, Dict[str, float]] = {}
        for criterion, model_scores in criterion_outputs.items():
            for m, raw_score in model_scores.items():
                metrics_per_model.setdefault(m, {})[criterion] = raw_score / 2
        for m, bloom_info in new_bloom_dict["bloom_alignments_per_model"].items():
            metrics_per_model.setdefault(m, {})["bloom_level_alignment"] = bloom_info["bloom_alignment_score"]
        result["metrics_per_model"] = metrics_per_model

        return result