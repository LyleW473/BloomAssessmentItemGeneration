def _get_metrics_per_model_from_result(result: Dict[str, Any]) -> Optional[Dict[str, Dict[str, float]]]:
    """
    Extracts per-evaluator-model normalized criterion metrics from a criterion result dict.

    Uses 'metrics_per_model' when available, reconstructs criterion metrics from
    'raw_scores' otherwise, and always attempts to add per-model BLA scores from
    bloom_alignment_details.
    """
    mpm: Dict[str, Dict[str, float]] = {}

    # Format: Already normalized per-model metrics
    if "metrics_per_model" in result and result["metrics_per_model"]:
        for model_name, metrics in result["metrics_per_model"].items():
            mpm[model_name] = dict(metrics)

    # Backward-compatible reconstruction from raw 0/1/2 scores
    raw_scores = result.get("raw_scores", {})
    for criterion, model_scores in raw_scores.items():
        for model_name, raw_score in model_scores.items():
            mpm.setdefault(model_name, {})[criterion] = raw_score / 2

    # Always add per-model soft Bloom alignment score when available
    bloom_details = result.get("bloom_alignment_details", {})
    for model_name, bloom_info in bloom_details.get("bloom_alignments_per_model", {}).items():
        if "bloom_alignment_score" in bloom_info:
            mpm.setdefault(model_name, {})["bloom_level_alignment"] = bloom_info["bloom_alignment_score"]

    return mpm if mpm else None

def compute_inter_rater_metrics(
    pipeline_names: List[str],
    all_criterion_results: Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]],
    criteria: List[str],
    include_bloom_alignment: bool = False,
) -> Dict[str, Any]:
    """
    Computes inter-rater reliability metrics across evaluator (judge) models, per pipeline.

    For each (pipeline, criterion) pair, collects per-question normalized scores (0-1) from
    every evaluator model and reports:

      - mean_score:         mean normalized score across all evaluator models and questions.
      - agreement_rate:     fraction of questions (with >=2 evaluator models) where all models
                            gave the same raw integer score (0, 1, or 2); None if only one model.
      - mean_std:           mean per-question standard deviation of normalized scores across
                            evaluator models (0.0 = perfect agreement on every question); None
                            if only one model.
      - pairwise_pearson:   Pearson r between each evaluator model pair over all questions where
                            both models scored; omitted if < 2 models.
      - n:                  number of questions contributing.

    Only the criteria explicitly passed in 'criteria' are evaluated. Bloom-level label
    agreement is computed separately by 'compute_bloom_level_agreement()'.

    Args:
        pipeline_names: List of pipeline names.
        all_criterion_results: file -> pipeline -> generator_model -> list[criterion_results]
        criteria: List of non-Bloom rubric criterion names.
        include_bloom_alignment: Whether to include 'bloom_level_alignment' as a criterion if not already in 'criteria'.
    Returns:
        pipeline_name -> {
            "per_criterion": {
                criterion_name: {
                    "mean_score": float | None,
                    "agreement_rate": float | None,
                    "mean_std": float | None,
                    "pairwise_pearson": {"modelA__vs__modelB": float} | None,
                    "n": int,
                }
            },
            "n_evaluator_models": int,
        }
    """
    all_criteria = list(criteria)
    if include_bloom_alignment and "bloom_level_alignment" not in all_criteria:
        all_criteria.append("bloom_level_alignment")
    output: Dict[str, Any] = {}

    for pipeline_name in pipeline_names:
        all_results_flat: List[Dict[str, Any]] = []
        for file_name, p_to_m_to_r in all_criterion_results.items():
            if pipeline_name not in p_to_m_to_r:
                continue
            for gen_model, results_list in p_to_m_to_r[pipeline_name].items():
                all_results_flat.extend(results_list)

        if not all_results_flat:
            output[pipeline_name] = {
                "per_criterion": {c: {"mean_score": None, "agreement_rate": None, "mean_std": None, "pairwise_pearson": None, "n": 0} for c in all_criteria},
                "n_evaluator_models": 0,
            }
            continue

        # Discover which evaluator models are present
        evaluator_models: Set[str] = set()
        for r in all_results_flat:
            mpm = _get_metrics_per_model_from_result(r)
            if mpm:
                evaluator_models.update(mpm.keys())
        evaluator_models_list = sorted(evaluator_models)
        n_evaluator_models = len(evaluator_models_list)

        per_criterion_stats: Dict[str, Any] = {}

        for criterion in all_criteria:
            # Build per-question score vectors: list of {model_name: normalized_score}
            per_question_scores: List[Dict[str, float]] = []
            for r in all_results_flat:
                mpm = _get_metrics_per_model_from_result(r)
                if mpm is None:
                    continue
                q_scores = {m: mpm[m][criterion] for m in evaluator_models_list if m in mpm and criterion in mpm[m]}
                if q_scores:
                    per_question_scores.append(q_scores)

            n = len(per_question_scores)
            if n == 0:
                per_criterion_stats[criterion] = {"mean_score": None, "agreement_rate": None, "mean_std": None, "pairwise_pearson": None, "n": 0}
                continue

            all_scores_flat = [s for q in per_question_scores for s in q.values()]
            mean_score = float(np.mean(all_scores_flat))

            if n_evaluator_models < 2:
                per_criterion_stats[criterion] = {
                    "mean_score": round(mean_score, 4),
                    "agreement_rate": None,
                    "mean_std": None,
                    "pairwise_pearson": None,
                    "n": n,
                }
                continue

            # Agreement rate: all models gave the same raw integer score on a question.
            # Recover integer scores: multiply normalised score by 2 and round for 0/1/2
            # criteria; for bloom_level_alignment (steps of 0.2) use round(score * 5).
            multi_q = [q for q in per_question_scores if len(q) >= 2]

            if criterion == "bloom_level_alignment":
                # BLA is in 0.2 increments, so recover the 0--5 gap bucket.
                agree_count = sum(
                    1 for q in multi_q
                    if len({round(s * 5) for s in q.values()}) == 1
                )
            else:
                # Ordinary rubric criteria are normalized raw scores 0/1/2 -> 0/0.5/1.
                agree_count = sum(
                    1 for q in multi_q
                    if len({round(s * 2) for s in q.values()}) == 1
                )
            agreement_rate = agree_count / len(multi_q) if multi_q else None

            # Mean per-question std across evaluator models
            per_q_stds = [float(np.std(list(q.values()))) for q in multi_q]
            mean_std = float(np.mean(per_q_stds)) if per_q_stds else None

            # Pairwise Pearson correlations
            pairwise_pearson: Dict[str, float] = {}
            for m_a, m_b in combinations(evaluator_models_list, 2):
                paired = [(q[m_a], q[m_b]) for q in per_question_scores if m_a in q and m_b in q]
                if len(paired) >= 2:
                    xs = np.array([p[0] for p in paired])
                    ys = np.array([p[1] for p in paired])
                    if np.std(xs) > 0 and np.std(ys) > 0:
                        r_val = float(np.corrcoef(xs, ys)[0, 1])
                    else:
                        r_val = 1.0 if np.array_equal(xs, ys) else 0.0
                    pairwise_pearson[f"{m_a}__vs__{m_b}"] = round(r_val, 4)

            per_criterion_stats[criterion] = {
                "mean_score": round(mean_score, 4),
                "agreement_rate": round(agreement_rate, 4) if agreement_rate is not None else None,
                "mean_std": round(mean_std, 4) if mean_std is not None else None,
                "pairwise_pearson": pairwise_pearson if pairwise_pearson else None,
                "n": n,
            }

        output[pipeline_name] = {
            "per_criterion": per_criterion_stats,
            "n_evaluator_models": n_evaluator_models,
        }

    return output


def compute_bloom_level_agreement(
    pipeline_names: List[str],
    all_criterion_results: Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]],
) -> Dict[str, Any]:
    """
    Computes inter-rater agreement between evaluator (judge) models on Bloom level
    classification, per pipeline.

    For each question the per-model Bloom level predictions are read from
    bloom_alignment_details.bloom_alignments_per_model.  Reports:

      - agreement_rate:             fraction of questions (with >=2 evaluator models) where
                                    all models predicted the same Bloom level.
      - per_level_agreement:        for each majority_true_bloom_level, the fraction of those
                                    questions that had unanimous consensus and how many
                                    contributed.
      - pairwise_percent_agreement: for each evaluator model pair, fraction of questions
                                    where both models predicted the same Bloom level.
      - vote_counts_aggregate:      total Bloom level vote distribution across all questions.
      - n:                          total questions with at least one model prediction.
      - n_evaluator_models:         number of distinct evaluator models seen.

    Args:
        pipeline_names: List of pipeline names.
        all_criterion_results: file -> pipeline -> generator_model -> list[criterion_results]

    Returns:
        pipeline_name -> {
            "agreement_rate": float | None,
            "per_level_agreement": {bloom_level: {"agreement_rate": float | None, "n": int}},
            "pairwise_percent_agreement": {"modelA__vs__modelB": float},
            "vote_counts_aggregate": {bloom_level: int},
            "n": int,
            "n_multi": int,
            "n_evaluator_models": int,
        }
    """
    output: Dict[str, Any] = {}

    for pipeline_name in pipeline_names:
        all_results_flat: List[Dict[str, Any]] = []
        for file_name, p_to_m_to_r in all_criterion_results.items():
            if pipeline_name not in p_to_m_to_r:
                continue
            for gen_model, results_list in p_to_m_to_r[pipeline_name].items():
                all_results_flat.extend(results_list)

        if not all_results_flat:
            output[pipeline_name] = {
                "agreement_rate": None,
                "per_level_agreement": {},
                "pairwise_percent_agreement": {},
                "vote_counts_aggregate": {},
                "n": 0,
                "n_multi": 0,
                "n_evaluator_models": 0,
            }
            continue

        # Discover evaluator models
        evaluator_models: Set[str] = set()
        for r in all_results_flat:
            per_model = r.get("bloom_alignment_details", {}).get("bloom_alignments_per_model", {})
            evaluator_models.update(per_model.keys())
        evaluator_models_list = sorted(evaluator_models)
        n_evaluator_models = len(evaluator_models_list)

        n = 0
        n_multi = 0           # questions with >=2 evaluator model predictions
        agreement_count = 0   # unanimous full agreement

        per_level_total: Dict[str, int] = {}
        per_level_agreed: Dict[str, int] = {}
        pairwise_agree: Dict[str, int] = {}
        pairwise_total: Dict[str, int] = {}
        vote_counts_aggregate: Dict[str, int] = {}

        for r in all_results_flat:
            bloom_details = r.get("bloom_alignment_details", {})
            per_model = bloom_details.get("bloom_alignments_per_model", {})
            if not per_model:
                continue

            model_bloom = {
                m: per_model[m]["true_bloom_level"]
                for m in evaluator_models_list
                if m in per_model and "true_bloom_level" in per_model[m]
            }
            if not model_bloom:
                continue

            n += 1
            for level in model_bloom.values():
                vote_counts_aggregate[level] = vote_counts_aggregate.get(level, 0) + 1

            majority_level = bloom_details.get("majority_true_bloom_level")
            if majority_level:
                per_level_total[majority_level] = per_level_total.get(majority_level, 0) + 1

            if len(model_bloom) >= 2:
                n_multi += 1
                if len(set(model_bloom.values())) == 1:
                    agreement_count += 1
                    if majority_level:
                        per_level_agreed[majority_level] = per_level_agreed.get(majority_level, 0) + 1

                for m_a, m_b in combinations(sorted(model_bloom.keys()), 2):
                    pair_key = f"{m_a}__vs__{m_b}"
                    pairwise_total[pair_key] = pairwise_total.get(pair_key, 0) + 1
                    if model_bloom[m_a] == model_bloom[m_b]:
                        pairwise_agree[pair_key] = pairwise_agree.get(pair_key, 0) + 1

        per_level_agreement = {
            level: {
                "agreement_rate": round(per_level_agreed.get(level, 0) / total, 4) if total > 0 else None,
                "n": total,
            }
            for level, total in per_level_total.items()
        }

        pairwise_pct = {
            pair_key: round(pairwise_agree.get(pair_key, 0) / total, 4)
            for pair_key, total in pairwise_total.items()
        }

        output[pipeline_name] = {
            "agreement_rate": round(agreement_count / n_multi, 4) if n_multi > 0 else None,
            "per_level_agreement": per_level_agreement,
            "pairwise_percent_agreement": pairwise_pct,
            "vote_counts_aggregate": vote_counts_aggregate,
            "n": n,
            "n_multi": n_multi,
            "n_evaluator_models": n_evaluator_models,
        }

    return output