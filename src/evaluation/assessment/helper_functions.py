def extract_pipeline_name_from_file_name(file_name:str) -> str:
    """
    Extracts the pipeline name from the given file name.

    Args:
        file_name (str): The name of the file, containing the pipeline name and course name, e.g.,
                        'baseline___4CCSAIAI Introduction to Artificial Intelligence(24~25 SEM1 000001).json'.

    Returns:
        str: The extracted pipeline name.
    """
    return file_name.split("___")[0]

def compute_criteria_pass_rates(
    pipeline_names:List[str],
    all_criterion_results:Dict[str, Dict[str, List[Dict[str, Any]]]],
    criteria:List[str]
) -> Dict[str, Dict[str, Any]]:
    """
    Computes per-pipeline criteria pass rates across all files.
    
    A question 'passes' a criterion if it scores 1.0 (perfect score) on that criterion.
    This provides insight into how often each pipeline produces questions that fully
    satisfy each quality criterion.
    
    Args:
        pipeline_names (List[str]): List of pipeline names to evaluate.
        all_criterion_results (Dict[str, Dict[str, List[Dict[str, Any]]]]): Mapping from file_name -> pipeline_name -> list of criterion results
        criteria (List[str]): List of criterion names to evaluate.

    Returns:
        Dict[str, Dict[str, Any]]: A dictionary mapping pipeline_name -> {
                                                                    "overall_pass_rate": float (average across all criteria),
                                                                    "per_criterion": Dict mapping criterion -> pass_rate (0.0 to 1.0)
                                                                }
    """
    criteria_pass_rates = {}
    
    for pipeline_name in pipeline_names:
        # Aggregate all results for this pipeline across all files
        all_results = []
        for file_name in all_criterion_results:
            if pipeline_name in all_criterion_results[file_name]:
                all_results.extend(all_criterion_results[file_name][pipeline_name])
        
        total_questions = len(all_results)
        pass_rates = {}
        
        # Calculate pass rate for each criterion
        for criterion in criteria:
            passed = sum(1 for r in all_results if r["metrics"].get(criterion, 0) == 1)
            pass_rates[criterion] = passed / total_questions if total_questions > 0 else 0
        
        # Calculate overall pass rate
        criteria_pass_rates[pipeline_name] = {
            "overall_pass_rate": sum(pass_rates.values()) / len(pass_rates) if pass_rates else 0,
            "per_criterion": pass_rates
        }
    return criteria_pass_rates

def aggregate_criterion_results_per_pipeline_global(
        all_criterion_results: Dict[str, Dict[str, List[Dict[str, Any]]]]
    ) -> Dict[str, List[Dict[str, Any]]]:
    """
    Aggregates all criterion results per pipeline across all files.
    
    This combines results from different files into a single list per pipeline,
    useful for calculating overall statistics and metrics across the entire dataset.
    Args:
        all_criterion_results (Dict[str, Dict[str, List[Dict[str, Any]]]]): Mapping from file_name -> pipeline_name -> list of criterion results
    Returns:
        Dict[str, List[Dict[str, Any]]]: A dict mapping pipeline_name -> list of all criterion results across all files.
    """
    aggregated_results = {}
    
    for file_name, pipeline_to_results in all_criterion_results.items():
        for pipeline_name, criterion_results in pipeline_to_results.items():
            if pipeline_name not in aggregated_results:
                aggregated_results[pipeline_name] = []
            aggregated_results[pipeline_name].extend(criterion_results)
    
    return aggregated_results

def compute_average_criterion_metrics_per_pipeline_global(
        aggregated_criterion_results_per_pipeline:Dict[str, List[Dict[str, Any]]],
        criteria:List[str]
    ) -> Dict[str, Dict[str, Any]]:
    """
    Computes average metrics per criterion for each pipeline across all aggregated results.
    
    This calculates the mean score for each quality criterion (e.g., bloom_alignment, clarity)
    across all questions generated by each pipeline. Also includes the total yield (number of
    questions) for each pipeline.
    Args:
        aggregated_criterion_results_per_pipeline (Dict[str, List[Dict[str, Any]]]): Mapping from pipeline_name -> list of all criterion results.
        criteria (List[str]): List of criterion names to evaluate.

    Returns:
        Dict[str, Dict[str, Any]]: A dict mapping pipeline_name -> {
            criterion_name: average_score (0.0 to 1.0),
            ...
            "yield": total number of questions
        }
    """
    average_criterion_metrics_per_pipeline = {}
    
    for pipeline_name, all_criterion_results_for_pipeline in aggregated_criterion_results_per_pipeline.items():
        
        # Aggregate all 'metrics' dicts from the criterion results
        aggregated_metrics_counts = {criterion: 0 for criterion in criteria}
        
        total_results = len(all_criterion_results_for_pipeline)
        
        for result in all_criterion_results_for_pipeline:
            metrics = result.get("metrics", {})
            for key in aggregated_metrics_counts.keys():
                aggregated_metrics_counts[key] += metrics.get(key, 0)

        # Calculate average score for each criterion
        if total_results > 0:
            average_metrics = {key: value / total_results for key, value in aggregated_metrics_counts.items()}
        else:
            average_metrics = {key: 0.0 for key in aggregated_metrics_counts.keys()}
        
        average_criterion_metrics_per_pipeline[pipeline_name] = {
            **average_metrics,
            "yield": total_results  # Total number of questions generated across all files
        }
    
    return average_criterion_metrics_per_pipeline

def aggregate_per_criterion_scores(
        pipeline_names:List[str],
        criteria:List[str],
        aggregated_criterion_results_per_pipeline_global:Dict[str, List[Dict[str, Any]]]
    ) -> Dict[str, Dict[str, List[float]]]:
    """
    Aggregates individual criterion scores per pipeline for discrimination power calculations.
    
    This function collects all individual scores (not means) for each criterion from each pipeline,
    useful for calculating variance-based metrics like discrimination power.
    
    Args:
        pipeline_names (List[str]): List of pipeline names
        criteria (List[str]): List of criterion names to collect scores for
        aggregated_criterion_results_per_pipeline_global (Dict[str, List[Dict[str, Any]]]): 
            Dictionary mapping pipeline_name -> list of criterion result dictionaries
    
    Returns:
        Dict[str, Dict[str, List[float]]]: A nested dictionary mapping 
            pipeline_name -> criterion_name -> list of individual scores
            Example: {"zero_shot": {"bloom_level_alignment": [0.8, 1.0, 0.9], "clarity": [0.7, 0.8, 1.0] ... }}
    """
    # Initialize nested dictionary structure
    per_criterion_scores = {}
    for pipeline_name in pipeline_names:
        per_criterion_scores[pipeline_name] = {criterion: [] for criterion in criteria}

    # Collect individual scores from all results
    for pipeline_name, all_results in aggregated_criterion_results_per_pipeline_global.items():
        for result in all_results:
            metrics = result.get("metrics", {})
            for criterion in per_criterion_scores[pipeline_name].keys():
                per_criterion_scores[pipeline_name][criterion].append(metrics.get(criterion, 0))
    return per_criterion_scores

def calculate_discrimination_weights(
        pipeline_names:List[str],
        criteria:List[str],
        per_criterion_scores:Dict[str, Dict[str, List[float]]],
        verbose:bool=True
    ) -> Tuple[Dict[str, float], Dict[str, float]]:
    """
    Calculates discrimination-based weights for each criterion using range-based power.
    
    This function computes discrimination power as the range (max - min) of mean scores across
    pipelines for each criterion. A square root transformation is applied to reduce dominance
    of high-variance criteria, and weights are normalized to sum to 1.0.
    
    Args:
        pipeline_names (List[str]): List of pipeline names
        criteria (List[str]): List of criterion names
        per_criterion_scores (Dict[str, Dict[str, List[float]]]): Nested dictionary mapping
            pipeline_name -> criterion_name -> list of individual scores
        verbose (bool): If True, prints mean scores per pipeline for each criterion. Default is True.
    
    Returns:
        Tuple[Dict[str, float], Dict[str, float]]: A tuple containing:
            - discrimination_power: Raw discrimination power (range) for each criterion
            - discrimination_weights: Normalized weights (sum to 1.0) for each criterion
    """
    # Calculate discrimination power (range of mean scores across pipelines) for each criterion
    discrimination_power = {}
    for criterion in criteria:
        scores = [per_criterion_scores[p][criterion] for p in pipeline_names]
        mean_scores_per_pipeline = [sum(s)/len(s) if len(s) > 0 else 0 for s in scores]
        
        if verbose:
            print(f"Criterion: {criterion} | Mean scores per pipeline: {mean_scores_per_pipeline}")
        
        discrimination_power[criterion] = max(mean_scores_per_pipeline) - min(mean_scores_per_pipeline)

    # Apply SQUARE ROOT transformation to reduce dominance of high-variance criteria
    transformed_power = {k: np.sqrt(v) for k, v in discrimination_power.items()}

    # Normalize to sum to 1.0
    total_discrimination = sum(transformed_power.values())
    if total_discrimination > 0:
        discrimination_weights = {k: v / total_discrimination for k, v in transformed_power.items()}
    else:
        # Fallback to equal weights if all discrimination powers are zero
        discrimination_weights = {k: 1.0 / len(criteria) for k in criteria}
    
    # Sort discrimination power and weights by weight value in descending order for better interpretability
    sorted_discrimination_power = {k: discrimination_power[k] for k in sorted(discrimination_power.keys(), key=lambda x: discrimination_weights[x], reverse=True)}
    sorted_discrimination_weights = {k: discrimination_weights[k] for k in sorted(discrimination_weights.keys(), key=lambda x: discrimination_weights[x], reverse=True)}
    return sorted_discrimination_power, sorted_discrimination_weights

def calculate_weighted_pipeline_scores(
        pipeline_names: List[str],
        criteria: List[str],
        per_criterion_scores: Dict[str, Dict[str, List[float]]],
        discrimination_weights: Dict[str, float]
    ) -> Dict[str, float]:
    """
    Calculates the weighted score for each pipeline by applying the discrimination weights to the mean scores of each criterion.

    Args:
        pipeline_names (List[str]): List of pipeline names
        criteria (List[str]): List of criterion names
        per_criterion_scores (Dict[str, Dict[str, List[float]]]): Nested dictionary mapping
            pipeline_name -> criterion_name -> list of individual scores
        discrimination_weights (Dict[str, float]): Dictionary mapping criterion_name -> weight
    
    Returns:
        Dict[str, float]: Dictionary mapping pipeline_name -> weighted_score
    """

    # Calculate weighted scores for each pipeline
    weighted_scores = {}
    for pipeline_name in pipeline_names:
        weighted_score = 0.0
        for criterion in criteria:
            scores = per_criterion_scores[pipeline_name][criterion]
            mean_score = sum(scores) / len(scores) if scores else 0
            weighted_score += mean_score * discrimination_weights[criterion]
        weighted_scores[pipeline_name] = weighted_score

    # Sort pipelines by weighted score in descending order
    sorted_weighted_scores = {k: weighted_scores[k] for k in sorted(weighted_scores.keys(), key=lambda x: weighted_scores[x], reverse=True)}
    return sorted_weighted_scores

def compute_bloom_derived_metrics_per_pipeline(
        aggregated_criterion_results_per_pipeline: Dict[str, List[Dict[str, Any]]],
        higher_order_bloom_levels: Set[str]={"analyze", "synthesis", "evaluation"},
        all_bloom_levels:Set[str]={"knowledge", "understanding", "application", "analyze", "synthesis", "evaluation"}

    ) -> Dict[str, Dict[str, float]]:
    """
    Computes Bloom-derived aggregate metrics per pipeline.

    Per question:
        - bloom_alignment_accuracy: 1 if declared_bloom_level == majority_true_bloom_level, else 0
        - higher_order_success_rate: among questions where declared_bloom_level is higher-order,
        the fraction where majority_true_bloom_level exactly matches the declared level.

    These are then averaged across all questions to produce pipeline-level rates.

    Args:
        aggregated_criterion_results_per_pipeline (Dict[str, List[Dict[str, Any]]]): Mapping from
            pipeline_name -> list of all criterion results across all files.
        higher_order_bloom_levels (Set[str]): A set of Bloom levels considered higher-order.

    Returns:
        Dict[str, Dict[str, float]]: A dict mapping pipeline_name -> {
            "bloom_alignment_accuracy": float,
            "higher_order_success_rate": float
        }
    """
    bloom_metrics = {}

    for pipeline_name, results in aggregated_criterion_results_per_pipeline.items():
        total = len(results)
        if total == 0:
            bloom_metrics[pipeline_name] = {
                "bloom_alignment_accuracy": 0.0,
                "higher_order_success_rate": 0.0
            }
            continue

        alignment_correct = 0
        declared_higher_order_total = 0
        higher_order_success_count = 0

        for result in results:
            bloom_details = result.get("bloom_alignment_details", {})
            declared = bloom_details.get("declared_bloom_level")
            majority_true = bloom_details.get("majority_true_bloom_level")
            assert declared in all_bloom_levels or declared is None, f"Declared Bloom level '{declared}' not in known levels"
            assert majority_true in all_bloom_levels or majority_true is None, f"Majority true Bloom level '{majority_true}' not in known levels"

            if declared is None or majority_true is None:
                continue

            # Overall Bloom alignment accuracy
            if declared == majority_true:
                alignment_correct += 1

            # Higher-order success rate: exact match, restricted to higher-order declared levels
            if declared in higher_order_bloom_levels:
                declared_higher_order_total += 1
                if declared == majority_true:
                    higher_order_success_count += 1

        bloom_metrics[pipeline_name] = {
            "bloom_alignment_accuracy": alignment_correct / total,
            "higher_order_success_rate": (
                higher_order_success_count / declared_higher_order_total
                if declared_higher_order_total > 0 else 0.0
            ),
        }

    return bloom_metrics


def aggregate_criterion_results_per_pipeline_model_global(
        all_criterion_results:Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]]
    ) -> Dict[str, Dict[str, List[Dict[str, Any]]]]:
    """
    Aggregates all criterion results per pipeline, per model, across all files.

    Args:
        all_criterion_results: file_name -> pipeline_name -> model_name -> list[criterion_results]

    Returns:
        pipeline_name -> model_name -> list of all criterion results across files
    """
    aggregated: Dict[str, Dict[str, List[Dict[str, Any]]]] = {}
    for file_name, pipeline_to_model_to_results in all_criterion_results.items():
        for pipeline_name, model_to_results in pipeline_to_model_to_results.items():
            if pipeline_name not in aggregated:
                aggregated[pipeline_name] = {}
            for model_name, results in model_to_results.items():
                aggregated[pipeline_name].setdefault(model_name, []).extend(results)
    return aggregated


def compute_average_criterion_metrics_per_pipeline_model_global(
        aggregated_per_pipeline_model:Dict[str, Dict[str, List[Dict[str, Any]]]],
        criteria:List[str]
    ) -> Dict[str, Dict[str, Dict[str, Any]]]:
    """
    Computes average criterion metrics for each (pipeline, model) pair across all aggregated results.

    Args:
        aggregated_per_pipeline_model: pipeline_name -> model_name -> list[criterion_results]
        criteria: List of criterion names to evaluate.

    Returns:
        pipeline_name -> model_name -> {criterion_name: avg_score, ..., "yield": n}
    """
    output: Dict[str, Dict[str, Dict[str, Any]]] = {}
    for pipeline_name, model_to_results in aggregated_per_pipeline_model.items():
        output[pipeline_name] = {}
        for model_name, results in model_to_results.items():
            total = len(results)
            counts = {c: 0 for c in criteria}
            for r in results:
                metrics = r.get("metrics", {})
                for c in criteria:
                    counts[c] += metrics.get(c, 0)
            avg = {c: counts[c] / total for c in criteria} if total > 0 else {c: 0.0 for c in criteria}
            output[pipeline_name][model_name] = {**avg, "yield": total}
    return output


def compute_bloom_derived_metrics_per_pipeline_model(
        aggregated_per_pipeline_model: Dict[str, Dict[str, List[Dict[str, Any]]]],
        higher_order_bloom_levels: Set[str] = frozenset({"analyze", "synthesis", "evaluation"}),
        all_bloom_levels: Set[str] = frozenset({"knowledge", "understanding", "application", "analyze", "synthesis", "evaluation"})
    ) -> Dict[str, Dict[str, Dict[str, float]]]:
    """
    Computes Bloom-derived aggregate metrics for each (pipeline, model) pair.

    Args:
        aggregated_per_pipeline_model: pipeline_name -> model_name -> list[criterion_results]
        higher_order_bloom_levels: Bloom levels considered higher-order.
        all_bloom_levels: All known Bloom levels.

    Returns:
        pipeline_name -> model_name -> {"bloom_alignment_accuracy": float, "higher_order_success_rate": float}
    """
    results: Dict[str, Dict[str, Dict[str, float]]] = {}
    for pipeline_name, model_to_results in aggregated_per_pipeline_model.items():
        results[pipeline_name] = {}
        for model_name, criterion_results_list in model_to_results.items():
            total = len(criterion_results_list)
            if total == 0:
                results[pipeline_name][model_name] = {
                    "bloom_alignment_accuracy": 0.0,
                    "higher_order_success_rate": 0.0,
                }
                continue
            alignment_correct = 0
            declared_higher_order_total = 0
            higher_order_success_count = 0
            for r in criterion_results_list:
                bloom_details = r.get("bloom_alignment_details", {})
                declared = bloom_details.get("declared_bloom_level")
                majority_true = bloom_details.get("majority_true_bloom_level")
                assert declared in all_bloom_levels or declared is None, f"Declared Bloom level '{declared}' not in known levels"
                assert majority_true in all_bloom_levels or majority_true is None, f"Majority true Bloom level '{majority_true}' not in known levels"
                if declared is None or majority_true is None:
                    continue
                if declared == majority_true:
                    alignment_correct += 1
                if declared in higher_order_bloom_levels:
                    declared_higher_order_total += 1
                    if declared == majority_true:
                        higher_order_success_count += 1
            results[pipeline_name][model_name] = {
                "bloom_alignment_accuracy": alignment_correct / total,
                "higher_order_success_rate": (
                    higher_order_success_count / declared_higher_order_total
                    if declared_higher_order_total > 0 else 0.0
                ),
            }
    return results


def compute_criteria_pass_rates_by_model(
        pipeline_names: List[str],
        all_criterion_results: Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]],
        criteria: List[str]
    ) -> Dict[str, Dict[str, Dict[str, Any]]]:
    """
    Computes per-(pipeline, model) criteria pass rates across all files.

    A question 'passes' a criterion if it scores 1.0 on that criterion.

    Args:
        pipeline_names: List of pipeline names to evaluate.
        all_criterion_results: file_name -> pipeline_name -> model_name -> list[criterion_results]
        criteria: List of criterion names to evaluate.

    Returns:
        pipeline_name -> model_name -> {"overall_pass_rate": float, "per_criterion": Dict}
    """
    pass_rates: Dict[str, Dict[str, Dict[str, Any]]] = {}
    for pipeline_name in pipeline_names:
        pass_rates[pipeline_name] = {}
        model_to_results: Dict[str, List[Dict[str, Any]]] = {}
        for file_name, p_to_m_to_r in all_criterion_results.items():
            if pipeline_name not in p_to_m_to_r:
                continue
            for model_name, results in p_to_m_to_r[pipeline_name].items():
                model_to_results.setdefault(model_name, []).extend(results)
        for model_name, all_results in model_to_results.items():
            total = len(all_results)
            per_criterion = {}
            for criterion in criteria:
                passed = sum(1 for r in all_results if r["metrics"].get(criterion, 0) == 1)
                per_criterion[criterion] = passed / total if total > 0 else 0.0
            pass_rates[pipeline_name][model_name] = {
                "overall_pass_rate": sum(per_criterion.values()) / len(per_criterion) if per_criterion else 0.0,
                "per_criterion": per_criterion,
            }
    return pass_rates


def build_matched_task_mapping(
        file_to_pipeline_to_model_to_saqs_mapping: Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]],
        all_criterion_results: Dict[str, Dict[str, Dict[str, List[Dict[str, Any]]]]]
    ) -> Dict[Tuple[str, str], Dict[str, Dict[str, Dict[str, List]]]]:
    """
    Groups questions and criterion results by (file_path, difficulty) task key.

    A "task" is defined as a (source_file_path, difficulty) slot — the same content chunk
    and intended difficulty level.  Questions from different pipelines that share the same
    task slot were generated from the same source material at the same difficulty target,
    making them directly comparable.

    Args:
        file_to_pipeline_to_model_to_saqs_mapping: file_path -> pipeline -> model -> list[questions]
        all_criterion_results: file_path -> pipeline -> model -> list[criterion results]
            (index-aligned to the corresponding questions)

    Returns:
        Dict mapping (file_path, difficulty) ->
            pipeline_name -> model_name -> {"questions": [...], "criteria": [...]}
    """
    task_mapping: Dict[Tuple[str, str], Dict[str, Dict[str, Dict[str, List]]]] = {}

    for file_path, p_to_m_to_q in file_to_pipeline_to_model_to_saqs_mapping.items():
        for pipeline_name, m_to_q in p_to_m_to_q.items():
            for model_name, questions in m_to_q.items():
                criteria_list = (
                    all_criterion_results
                    .get(file_path, {})
                    .get(pipeline_name, {})
                    .get(model_name, [])
                )
                for i, q in enumerate(questions):
                    if i >= len(criteria_list):
                        continue  # skip questions without a criterion result
                    difficulty = q.get("difficulty", "unknown")
                    task_key = (file_path, difficulty)

                    slot = (
                        task_mapping
                        .setdefault(task_key, {})
                        .setdefault(pipeline_name, {})
                        .setdefault(model_name, {"questions": [], "criteria": []})
                    )
                    slot["questions"].append(q)
                    slot["criteria"].append(criteria_list[i])

    return task_mapping


def select_question_for_task(
        questions:List[Dict[str, Any]],
        criteria:List[Dict[str, Any]],
        mode:str,
        discrimination_weights:Dict[str, float],
    ) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """
    Selects a representative (question, criterion) pair for a task slot.

    raw = returns the first generated question (index 0), representing intrinsic
          generation quality before any selection or filtering.
    production = returns the question with the highest discrimination-weighted criterion
                 score, representing end-to-end pipeline quality after selection.

    Args:
        questions (List[Dict[str, Any]]): list of questions for this task slot (index-aligned with criteria)
        criteria (List[Dict[str, Any]]): list of criterion result dicts, aligned with questions
        mode (str): "raw" or "production" representing the selection strategy
        discrimination_weights (Dict[str, float]): criterion_name -> normalised weight (sum to 1.0)

    Returns:
        (question, criterion_result) tuple
    """
    assert questions and criteria and len(questions) == len(criteria), \
        "questions and criteria must be non-empty and index-aligned"

    if mode == "raw":
        return questions[0], criteria[0]

    if mode == "production":
        def _score(crit_result: Dict[str, Any]) -> float:
            metrics = crit_result.get("metrics", {})
            return sum(metrics.get(k, 0.0) * w for k, w in discrimination_weights.items())

        best_idx = max(range(len(criteria)), key=lambda i: _score(criteria[i]))
        return questions[best_idx], criteria[best_idx]

    raise ValueError(f"Unknown mode: '{mode}'. Use 'raw' or 'production'.")

def compute_yield_metrics(
        pipeline_names:List[str],
        task_mapping:Dict[Tuple[str, str], Dict[str, Dict[str, Dict[str, List]]]],
        discrimination_weights:Dict[str, float],
        validity_threshold:float=0.5,
    ) -> Dict[str, Dict[str, Any]]:
    """
    Computes yield metrics per pipeline, treating yield as a *systems* metric separate from
    pairwise quality comparisons.

    For each pipeline, for every task slot (file_path, difficulty) in which the pipeline
    participated:
        - a question is considered "valid" if its discrimination-weighted criterion score
          meets 'validity_threshold'.
        - task_yield_rate = proportion of task slots containing >= 1 valid question
        - mean_valid_per_task = average number of valid questions per task slot
        - total_valid = total number of valid questions across all task slots
        - total_questions = total questions generated across all task slots
    
    Args:
        pipeline_names (List[str]): list of pipeline names
        task_mapping (Dict[Tuple[str, str], Dict[str, Dict[str, Dict[str, List]]]]): output of 'build_matched_task_mapping'
        discrimination_weights (Dict[str, float]): criterion_name -> normalised weight (sum to 1.0)
        validity_threshold (float): minimum weighted score to consider a question valid. 
    
    Returns:
        Dict[str, Dict[str, Any]]: Mapping pipeline_name -> {
            "task_yield_rate": float,
            "mean_valid_per_task": float,
            "total_valid": int,
            "total_questions": int,
            "total_task_slots": int,
        }
    """
    def _score(crit_result:Dict[str, Any]) -> float:
        """
        Calculates the discrimination-weighted score for a given criterion result dict.

        Args:
            crit_result (Dict[str, Any]): A dictionary containing at least a "metrics" key,
                                          which is itself a dict mapping criterion names to 
                                          their scores for this question.
        """
        metrics = crit_result.get("metrics", {})
        total_w = sum(discrimination_weights.values())
        if total_w <= 0:
            return 0.0
        return sum(metrics.get(k, 0.0) * w for k, w in discrimination_weights.items()) / total_w

    results: Dict[str, Dict[str, Any]] = {}

    for pipeline_name in pipeline_names:
        task_slots_with_pipeline = 0
        task_slots_with_valid = 0
        total_valid = 0
        total_questions = 0

        for task_key, p_to_m_to_data in task_mapping.items():
            if pipeline_name not in p_to_m_to_data:
                continue

            task_slots_with_pipeline += 1
            slot_has_valid = False
            slot_valid_count = 0
            slot_total = 0

            for model_name, data in p_to_m_to_data[pipeline_name].items():
                for crit in data["criteria"]:
                    slot_total += 1
                    if _score(crit) >= validity_threshold:
                        slot_has_valid = True
                        slot_valid_count += 1

            total_questions += slot_total
            total_valid += slot_valid_count
            if slot_has_valid:
                task_slots_with_valid += 1

        results[pipeline_name] = {
            "task_yield_rate": task_slots_with_valid / task_slots_with_pipeline if task_slots_with_pipeline > 0 else 0.0,
            "mean_valid_per_task": total_valid / task_slots_with_pipeline if task_slots_with_pipeline > 0 else 0.0,
            "total_valid": total_valid,
            "total_questions": total_questions,
            "total_task_slots": task_slots_with_pipeline,
        }

    return results


